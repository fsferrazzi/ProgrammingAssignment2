cacheSolve <- function(x, ...) {
  inv <- x$getInv()
  if(!is.null(inv)) {
    message("getting cached data")
    return(inv)
  }
  A <- x$get()
  inv <- solve(A)
  x$setInv(inv)
  inv
}
